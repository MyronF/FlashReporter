package myron.com.crimereporter;

public class XYvalue {
    private String x;
    private double y;

    public XYvalue(String x, double y) {
     this.x = x;
     this.y = y;
    }

    public String getX(){
        return x;
    }

    public void setX(){
        this.x = x;
    }

    public double getY(){
        return y;
    }

    public void setY(){
        this.y = y;
    }
}
